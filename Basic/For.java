public class For {
    public static void main(String[] args) {
        
        int i = 1;
        
        if (i <= 5) {
            System.out.println("Print john");
        }

        i = i + 1; 

        for (int a = 1; a <= 5; a++) {
            System.out.println("print jaskar");
        }

        for (int b = 1; b <= 10; b++) {
            System.out.println("john");
        }

        for (int c = 1; c <= 10; c++) {
            System.out.println(c);
        }
        
        //reverse 
        for (int d = 5; d>=1; d--) {
            System.out.println(d);
        }
    }
}
   